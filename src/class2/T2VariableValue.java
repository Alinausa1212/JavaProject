package class2;
/*
Create a Java Program and name it Variable Values:
- In your program declare variables using
   different ways to store all different primitive data
- Update value of all variables
- Print the value of each updated variables
 */
public class T2VariableValue {
    public static void main(String[] args) {
        byte age=75;
        age=78;
        System.out.println(age);

        short price=1000;
        price=11;
        System.out.println(price);

        int salary=120000;
        salary=130000;
        System.out.println(salary);

        long distance=12545551112122L;
        distance=125122;
        System.out.println(distance);

        float number1=-12521.1111F;
        number1=122.2f;


        double num2=1325.3325;
        num2=12.2;
        System.out.println(num2);

        char symbol='&';
        symbol='4';
        System.out.println(symbol);

        boolean isRich=false;
        isRich=true;
        System.out.println(isRich);

        String city="London";
        city="New York";
        System.out.println(city);




    }
}
